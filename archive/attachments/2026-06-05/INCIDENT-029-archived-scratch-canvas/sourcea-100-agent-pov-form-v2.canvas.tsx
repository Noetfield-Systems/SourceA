const AGENT_FILTER = [
  { value: "all", label: "All agents (100)" },
  ...AGENTS.map((a) => ({ value: a.id, label: `${a.label} POV` })),
];

const PHASE_FILTER = [
  { value: "all", label: "All phases" },
  ...PHASES.map((p) => ({ value: String(p.id), label: `Phase ${p.id}` })),
];

const VIEW_OPTIONS = [
  { value: "live-form", label: "Live form — pick & confirm" },
  { value: "by-agent", label: "By agent POV" },
  { value: "open-only", label: "Open / unconfirmed only" },
  { value: "checklist", label: "100-step checklist" },
];

function agentLabel(id: AgentId): string {
  return AGENTS.find((a) => a.id === id)?.label ?? id;
}

function getPick(q: FormQuestion, picks: Record<string, string>): string {
  return picks[q.id] || q.shipped || "";
}

function buildAsfBlock(confirmed: Record<string, boolean>, picks: Record<string, string>): string {
  const lines = ["ASF: FIVE-STEP — PICK: batch"];
  FORM_QUESTIONS.filter((q) => confirmed[q.id]).forEach((q) => {
    const pick = getPick(q, picks);
    lines.push(`${q.id} ${pick} — ${q.subject.toLowerCase()}`);
  });
  lines.push("Effect: Commercial v2 form · agent POV · Maintainer SHIP proof table");
  return lines.join("\n");
}

function QuestionCard(props: {
  q: FormQuestion;
  pick: string;
  onPick: (v: string) => void;
  comment: string;
  onComment: (v: string) => void;
  confirmed: boolean;
  onConfirmed: (v: boolean) => void;
}) {
  const { q, pick, onPick, comment, onComment, confirmed, onConfirmed } = props;
  const opt = q.options.find((o) => o.key === pick);
  return (
    <Card variant={confirmed ? "elevated" : "borderless"}>
      <CardHeader
        title={`[${q.id}] ${q.subject}`}
        subtitle={`${agentLabel(q.agent)} · Phase ${q.phase}${q.decisive ? " · DECISIVE" : " · AUDIT"}`}
      />
      <CardBody>
        <Stack gap={10}>
          <Text>{q.question}</Text>
          <Text size="small" tone="tertiary">On disk: {q.diskToday}</Text>
          {q.shipped ? <Pill tone="success" size="sm">Shipped: {q.shipped}</Pill> : null}
          <Select
            value={pick}
            onChange={onPick}
            options={q.options.map((o) => ({ value: o.key, label: `${o.key} — ${o.short}` }))}
          />
          {opt ? (
            <Callout tone="info" title="If you pick this">
              <Text size="small">{opt.ifYouPick}</Text>
            </Callout>
          ) : null}
          <TextArea label="Comment" value={comment} onChange={onComment} rows={2} placeholder="Ask for brief if unclear…" />
          <Row gap={8} style={{ alignItems: "center" }}>
            <Checkbox checked={confirmed} onChange={onConfirmed} label="I accept the Effect — confirm" />
            {confirmed ? <Pill tone="success" size="sm">Confirmed</Pill> : <Pill tone="neutral" size="sm">Open</Pill>}
          </Row>
          <Divider />
          <Text size="small" tone="tertiary">{q.maintainerAction}</Text>
        </Stack>
      </CardBody>
    </Card>
  );
}

